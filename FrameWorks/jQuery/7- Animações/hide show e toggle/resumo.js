/**SHOW, HIDE E TOGGLE 
 * 
 * Estes 3 métodos são utilizados para exibir, esconder e alternar a exibição de elementos HTML.
 *  obs1) Se você chamar o método sem parâmetros, o elemento aparecerá ou sumirá na hora.
 *  obs2) Ao passar um valor numérico, o elemento aparecerá com ume feito de expansão.
 * 
 * SHOW
 * $('div').show();
 * $('div').show(1000); -> Tempo para exibir em milisgundos.
 * 
 * HIDE
 * $('div').hide();
 * $('div').hide(1000);
 * 
 * TOGGLE
 * $('div').toggle();
 * $('div').toggle(1000);
 */