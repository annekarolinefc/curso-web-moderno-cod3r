/**SLIDE E FADE
 * 
 * A partir da lógica do hide e show, será facil entender os outros efeitos que são parecidos com eles.
 * 
 * $(".aqua").slideDown();
 * $(".aqua").slideUp();
 * $(".aqua").slideToggle();
 * 
 * $(".aqua").fadeOut();
 * $(".aqua").fadeIn();
 * $(".aqua").fadeToggle();
 * $(".aqua").fadeTp(500, 0.5);
 * 
 * 
 * SLIDE
 * os métodos slide são equivalentes aos hide e show.
 * $(".aqua").slideDown(); -> exibe o elemento com efeito de slide de cima para baixo
 * $(".aqua").slideUp(); -> oculta o elemento com efeito inverso
 * $(".aqua").slideToggle(); -> altera a visibilidade do elemento;
 * 
 * FADE
 * O fade possui 4 métodos, sendo os 3 primeironovamente a mesma lógica que a anterior.
 * $(".aqua").fadeIn(); -> exibe o elemento
 * $(".aqua").fadeOut(); -> oculta o elemento
 * $(".aqua").fadeToggle(); -> altera a visibilidade
 * $(".aqua").fadeTo(500, 0.5) -> PARAM 1: tempo de animação e PARAM 2: Transparencia de (0 a 1) 
 */